"""
Given a collection of intervals, merge all overlapping intervals.
Input: [[1,3],[2,6],[8,10], [9,11] ,[10,12], [2,4] ,[15,18]]
Output: [[1,6],[8,12],[15,18]] 

"""

class test:
    varu=[1,2,3]

    def updt(self):
        self.varu.copy
        self.varu=10


a=test()
b=test()
print(f"{a.varu}  {b.varu}")
a.updt()
print(f"{test.varu}  {test.varu}")